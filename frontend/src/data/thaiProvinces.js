/**
 * Calculate approximate road distance between two GPS coordinates.
 * Uses the Haversine formula multiplied by a 1.35 road factor
 * to approximate real road distance rather than straight-line.
 *
 * @param {number} lat1 - Latitude of point 1
 * @param {number} lng1 - Longitude of point 1
 * @param {number} lat2 - Latitude of point 2
 * @param {number} lng2 - Longitude of point 2
 * @returns {number} Approximate road distance in kilometers
 */
export function calculateDistance(lat1, lng1, lat2, lng2) {
  const R = 6371; // Earth's radius in km
  const toRad = (deg) => (deg * Math.PI) / 180;

  const dLat = toRad(lat2 - lat1);
  const dLng = toRad(lng2 - lng1);

  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(toRad(lat1)) *
      Math.cos(toRad(lat2)) *
      Math.sin(dLng / 2) *
      Math.sin(dLng / 2);

  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  const straightLineDistance = R * c;

  const ROAD_FACTOR = 1.35;
  return straightLineDistance * ROAD_FACTOR;
}

/**
 * Look up two provinces by name (Thai or English) and return
 * the approximate road distance between them in km, rounded to 1 decimal.
 *
 * @param {string} name1 - Province name (Thai or English)
 * @param {string} name2 - Province name (Thai or English)
 * @returns {number|null} Distance in km rounded to 1 decimal, or null if a province is not found
 */
export function getProvinceDistance(name1, name2) {
  const find = (name) =>
    provinces.find(
      (p) =>
        p.name_th === name ||
        p.name_en.toLowerCase() === name.toLowerCase()
    );

  const p1 = find(name1);
  const p2 = find(name2);

  if (!p1 || !p2) {
    return null;
  }

  const distance = calculateDistance(p1.lat, p1.lng, p2.lat, p2.lng);
  return Math.round(distance * 10) / 10;
}

/**
 * Get provinces that lie roughly along the route between two provinces.
 * Filters provinces whose perpendicular distance from the origin→destination
 * line is within a corridor, and that fall between origin and destination
 * along the route axis. Returns sorted by distance from origin.
 *
 * @param {string} originName - Origin province name (Thai or English)
 * @param {string} destName - Destination province name (Thai or English)
 * @param {number} corridorKm - Max perpendicular distance from route line (default 80km)
 * @returns {Array} Provinces along the route, sorted by distance from origin
 */
export function getProvincesAlongRoute(originName, destName, corridorKm = 80) {
  const find = (name) =>
    provinces.find(
      (p) => p.name_th === name || p.name_en.toLowerCase() === name.toLowerCase()
    );

  const origin = find(originName);
  const dest = find(destName);
  if (!origin || !dest) return provinces;

  const totalDist = calculateDistance(origin.lat, origin.lng, dest.lat, dest.lng);
  if (totalDist < 10) return provinces;

  return provinces
    .filter((p) => p.name_th !== origin.name_th && p.name_th !== dest.name_th)
    .map((p) => {
      const distFromOrigin = calculateDistance(origin.lat, origin.lng, p.lat, p.lng);
      const distFromDest = calculateDistance(dest.lat, dest.lng, p.lat, p.lng);
      // Use triangle: project point onto origin→dest line
      // If both distances are less than totalDist, it's roughly between them
      const s = (distFromOrigin + distFromDest + totalDist) / 2;
      const area = Math.sqrt(Math.max(0, s * (s - distFromOrigin) * (s - distFromDest) * (s - totalDist)));
      const perpDist = (2 * area) / totalDist;
      // Progress along route (0 = origin, 1 = dest)
      const progress = totalDist > 0
        ? (distFromOrigin * distFromOrigin - distFromDest * distFromDest + totalDist * totalDist) / (2 * totalDist * totalDist)
        : 0;
      return { ...p, perpDist, progress, distFromOrigin };
    })
    .filter((p) => p.perpDist <= corridorKm && p.progress > 0.05 && p.progress < 0.95)
    .sort((a, b) => a.progress - b.progress);
}

